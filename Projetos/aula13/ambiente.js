//ESTRUTURA DE REPETIÇÃO

//while (com teste no inicio)
var c = 1
while (c <= 6)
{
    console.log(`Passo ${c}`)
    c ++
}

//while (com o  teste final)
do
{
    console.log(`Passo ${c}`)
    c++
}
while(c <= 6)

//for (repetição com controle)
//for(inicio;teste;incremento)
for(var c=1; c<=10; c++)
{
    console.log(`Passo ${c}`)
}